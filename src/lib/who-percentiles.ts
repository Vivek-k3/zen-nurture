export function getWHOPercentiles(metric: "weight" | "length" | "head", gender: Gender): PercentileSet[] {
  if (metric === "weight") return gender === "male" ? weightBoys : weightGirls;
  if (metric === "length") return gender === "male" ? lengthBoys : lengthGirls;
  return gender === "male" ? headBoys : headGirls;
}

export function ageInMonths(dob: string, date: string): number {
  const birth = new Date(dob);
  const d = new Date(date);
  const months = (d.getFullYear() - birth.getFullYear()) * 12 + (d.getMonth() - birth.getMonth());
  const dayFraction = (d.getDate() - birth.getDate()) / 30;
  return Math.max(0, months + dayFraction);
}
